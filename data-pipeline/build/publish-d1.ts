// Publish the built warehouse.db to Cloudflare D1 — the single live source of
// truth for the writer/editorial agents (see docs). We keep building the local
// better-sqlite3 file exactly as before (fast, sync) and just PUBLISH it here:
// dump the whole DB to one SQL file (DROP+CREATE+INSERT, full replace), then
// `wrangler d1 execute --remote --file`. No re-plumbing of the pipeline.
//
// Requires: CLOUDFLARE_API_TOKEN + CLOUDFLARE_ACCOUNT_ID in the env, and a
// wrangler.toml with a [[d1_databases]] binding named "WAREHOUSE".
// Run: npm run publish:d1   (after build:warehouse)

import Database from 'better-sqlite3';
import { writeFileSync } from 'node:fs';
import { execFileSync } from 'node:child_process';
import { join } from 'node:path';
import { ROOT, WAREHOUSE_DIR } from '../lib/util';

const DB_PATH = join(WAREHOUSE_DIR, 'warehouse.db');
const DUMP_PATH = join(WAREHOUSE_DIR, 'warehouse-d1.sql');
const D1_NAME = process.env.D1_NAME || 'worldcup-site';
const INSERT_BATCH = 500; // rows per multi-row INSERT (keeps statements D1-friendly)

function sqlLiteral(v: unknown): string {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return Number.isFinite(v) ? String(v) : 'NULL';
  if (typeof v === 'bigint') return v.toString();
  if (Buffer.isBuffer(v)) return `X'${v.toString('hex')}'`;
  return `'${String(v).replace(/'/g, "''")}'`;
}

function main() {
  const db = new Database(DB_PATH, { readonly: true });
  const tables = db
    .prepare(`SELECT name, sql FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name`)
    .all() as { name: string; sql: string }[];

  const out: string[] = [
    '-- Kickabout warehouse → D1 full-replace dump. Generated by publish-d1.ts.',
    // Full replace: drop all tables first, then recreate — so we never trip over
    // FK/reference ordering. FKs off during load (D1 ignores PRAGMA foreign_keys
    // but harmless; local reload needs it off since we drop/recreate out of order).
    'PRAGMA foreign_keys = OFF;',
  ];
  // Drop every table up front (any order) before recreating, so cross-table
  // references during CREATE/INSERT never hit a missing table.
  for (const t of tables) out.push(`DROP TABLE IF EXISTS "${t.name}";`);

  let totalRows = 0;
  for (const t of tables) {
    out.push(`${t.sql};`);
    const cols = (db.prepare(`PRAGMA table_info("${t.name}")`).all() as { name: string }[]).map((c) => c.name);
    const colList = cols.map((c) => `"${c}"`).join(',');
    const rows = db.prepare(`SELECT ${colList} FROM "${t.name}"`).all() as Record<string, unknown>[];
    for (let i = 0; i < rows.length; i += INSERT_BATCH) {
      const chunk = rows.slice(i, i + INSERT_BATCH);
      const values = chunk.map((r) => `(${cols.map((c) => sqlLiteral(r[c])).join(',')})`).join(',');
      out.push(`INSERT INTO "${t.name}" (${colList}) VALUES ${values};`);
    }
    totalRows += rows.length;
    console.log(`  ${t.name}: ${rows.length} rows`);
  }

  // Views last — they read from tables, which now all exist. Writer-convenience
  // views (e.g. match_xg) publish to D1 too.
  const views = db
    .prepare(`SELECT name, sql FROM sqlite_master WHERE type='view' ORDER BY name`)
    .all() as { name: string; sql: string }[];
  for (const v of views) {
    out.push(`DROP VIEW IF EXISTS "${v.name}";`);
    out.push(`${v.sql};`);
    console.log(`  ${v.name}: (view)`);
  }

  writeFileSync(DUMP_PATH, out.join('\n'));
  console.log(`\nWrote ${DUMP_PATH} (${tables.length} tables, ${views.length} views, ${totalRows} rows)`);

  if (process.env.SKIP_D1_PUSH === '1') {
    console.log('SKIP_D1_PUSH=1 — dump written, not pushed.');
    return;
  }

  console.log(`Pushing to D1 "${D1_NAME}" (remote, full replace)…`);
  execFileSync(
    'npx',
    ['wrangler', 'd1', 'execute', D1_NAME, '--remote', '--yes', `--file=${DUMP_PATH}`, `--config=${join(ROOT, '..', 'wrangler.toml')}`],
    { stdio: 'inherit', cwd: ROOT },
  );
  console.log('D1 publish complete.');
}

main();
